package ch07.bean;	//패키지명
	
public class TestBean { //자바빈 클래스 정의
	
	private String id;	//id 프로퍼티
	private String age;
	private String name;
	
	
	public String getId() {
		return id;
	}
	public void setId(String id) {
		this.id = id;
	}
	public String getAge() {
		return age;
	}
	public void setAge(String age) {
		this.age = age;
	}
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	

	
}
